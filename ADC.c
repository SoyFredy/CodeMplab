char result; // Variable en 1 bytes
    
 

void puertos(void) // se declaran los puertos y salidas
{
   PORTA = 0x00; // Puerto A como salida.
   LATA = 0x00;
   TRISA = 0x00;
   
   PORTB = 0x00; //
   LATB = 0x00;
   TRISB = 0xF1; // RB0,RB4 - RB7 como entrada. RB1 - RB3 como salidas
}   

void ADC(void)
{
    ADCON1 = 0x00; //Se programa desde A0 hasta A12 y el voltaje de referencia VSS Y VDD
    ADCON2 = 0b10010100;//Se justifica a la derecha y dividiendo la freqz. osc. / 4 el tiempo de adq.
    ADCON0 = 0b00110011;//Se selecciona el canal 12 y encendiendo el convertidor.
}

void main(){
    puertos();
    ADC();
    ADCON0bits.GO_DONE = 1 ;// estatus del conversor
    while(1)
    {
        while( ADCON0bits.GO_DONE == 0)
        {
            PORTA = ADRESL;
            PORTBbits.RB1 = 0;
            if(ADRESL & 0x80)       //El 0x80=1000 0000
            {
                PORTBbits.RB1 = 1;
            }
            
            result = ADRESH;
            result <<= 2;
            result = (result & 0x0C);
            PORTB = (PORTB | result);
            ADCON0bits.GO_DONE = 1 ;
            
        }
    } 
}
